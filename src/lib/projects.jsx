export const GetProjectsMetaData = () => {
    const sortedByDateData = projectsData?.sort((a, b) => {
        const dateA = new Date(a.startDate || 0);
        const dateB = new Date(b.startDate || 0);
        return dateB - dateA;
    });

    return sortedByDateData?.map(
        ({
            slug = "",
            title = "",
            folderName = "",
            featuredImage = "",
            projectType = "",
            location = "",
            startDate = null,
        }) => ({
            slug,
            title,
            folderName,
            featuredImage,
            location,
            projectType,
            startDate,
        })
    );
};

const validateSlug = (slug = "") => {
    return slug
        .toString()
        .trim()
        .toLowerCase()
        .replace(/[^a-z0-9-]/g, "-")
        .replace(/-+/g, "-");
};

export const GetProjectsDataBySlug = (slug) => {
    return projectsData?.find(
        (project) => validateSlug(project.slug) === validateSlug(slug)
    );
};
